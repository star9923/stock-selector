def get_fallback_sector(code: str) -> str:
    """
    获取股票的备用板块
    :param code: 股票代码
    :return: 板块名称，如果未找到返回"未知"
    """
    return FALLBACK_SECTOR_MAPPING.get(code, "未知")


def update_sector_mapping_with_fallback(stock_board_map: dict) -> dict:
    """
    用备用映射补充板块映射
    :param stock_board_map: 现有的板块映射
    :return: 补充后的板块映射
    """
    if not stock_board_map:
        stock_board_map = {}

    # 用备用映射补充
    for code, sector in FALLBACK_SECTOR_MAPPING.items():
        if code not in stock_board_map:
            stock_board_map[code] = sector

    return stock_board_map
